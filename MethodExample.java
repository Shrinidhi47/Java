class MethodExample{
	public static void Name()
	{
		System.out.println("Name=Shrinidhi V Gudagudi");
	}
	
	public static void Email()
	{
		System.out.println("Email=shrinidhigudagudi@gmail.com");
	}
	public static void Address()
	{
		System.out.println("Address=India,Karnataka,Hangal,Ishwar Nagar");
	}
	public static void Name(String name)
	{
		System.out.println(name);
	}
}